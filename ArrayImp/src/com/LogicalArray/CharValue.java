package com.LogicalArray;

public class CharValue {
	public static void main(String[] args) {


		char a [] = new char[4];
		char b[]={'j','a','v','a'};
		for (char c : b) {
			 System.out.println(c);
	
		}
		
		char[] values = new char[4];
		values[0] = 'j';
		values[1] = 'a';
		values[2] = 'v';
		values[3] = 'a';
		// Loop over array with for-loop.
		for (char value : values) {
			System.out.println(value);
		}
		}

	}


